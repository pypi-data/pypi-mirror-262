def area(base1, base2, height):
    """
        This function returns the area of square with the provided input.

        Parameters:
        base1 (float): length of base1 of the trapezoid
        base2 (float): length of base2 of the trapezoid
        height (float): height of the trapezoid

        Returns:
        float: area of the trapezoid
    """
    return 0.5 * (base1 + base2) * height
